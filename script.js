function search(str) {
	let results = [];
	results = fruit.filter(fr => {
		return fr.toLowerCase().indexOf(str.toLowerCase()) > -1
	});
	return results;
}

function searchHandler(e) {
	if (e.target.value === "") return
	const text = e.target.value.trim().toLowerCase()
	suggestions.style.cssText = "display: block"
	showSuggestions(search(text), text);
}

function showSuggestions(results, inputVal) {
	let li = ""
	for(const index in results){
		li += "<li>" + results[index] + "</li>";
	}
	suggestions.innerHTML = li;
}

function useSuggestion(e) {
	input.value = e.target.innerText
	suggestions.style.cssText = "display: none"
}

input.addEventListener('keyup', searchHandler);
suggestions.addEventListener('click', useSuggestion);
